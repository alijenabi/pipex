#include "libft_init.h"

/**
 * Outputs the string 's' to the given file desciptor.
 * @param s[in]   The string to output.
 * @param fd[in]  The file descriptor on which to write.
 */
void	ft_putendl_fd(char *s, int fd)
{
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}
